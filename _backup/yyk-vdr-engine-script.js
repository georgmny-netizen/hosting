(function(){
  'use strict';
  function q(s){return document.querySelector(s)}
  function qa(s,r){return Array.prototype.slice.call((r||document).querySelectorAll(s))}
  
  function setVdrTab(key){
    qa('.yyk-vdr-tab').forEach(function(t){t.classList.toggle('active',t.getAttribute('data-vdr-tab')===key)});
    qa('.yyk-vdr-panel').forEach(function(p){p.classList.toggle('active',p.getAttribute('data-vdr-panel')===key)});
  }
  
  function runQualification(){
    var checks=qa('#vdrQualificationForm input[type="checkbox"]');
    var score=checks.filter(function(c){return c.checked}).length;
    var res=q('#vdrQualificationResult');
    if(res){
      res.className='yyk-vdr-result '+(score>=6?'ok':(score>=4?'':'warn'));
      res.textContent=(score>=6?'PRE-QUALIFIED: buyer may proceed to NDA/KYC VDR access. ':score>=4?'CONDITIONAL: missing evidence should be uploaded before committee review. ':'NOT READY: supplier evidence pack is incomplete. ')+'Score '+score+'/'+checks.length;
    }
  }
  
  function downloadReport(){
    var checks=qa('#vdrQualificationForm input[type="checkbox"]');
    var score=checks.filter(function(c){return c.checked}).length;
    var rows=checks.map(function(c){
      var label=(c.closest('label')||{}).textContent||c.name||'Control';
      return '<tr><td>'+label.trim().replace(/</g,'&lt;')+'</td><td>'+(c.checked?'READY':'MISSING')+'</td></tr>';
    }).join('');
    var html='<!doctype html><html><head><meta charset="utf-8"><title>Supplier Pre-Qualification Report</title></head><body><h1>Preliminary Supplier Qualification Report — YONGYEOKYO</h1><p class="ok">Score: '+score+'/'+checks.length+'</p><p>Generated by autonomous VDR Self-Qualification module. Final approval requires buyer NDA, KYC and manual compliance review.</p><table><tr><th>Control</th><th>Status</th></tr>'+rows+'</table></body></html>';
    var blob=new Blob([html],{type:'text/html'});
    var url=URL.createObjectURL(blob);
    var a=document.createElement('a');
    a.href=url;
    a.download='YONGYEOKYO_Preliminary_Supplier_Qualification_Report.html';
    a.click();
    URL.revokeObjectURL(url);
  }
  
  document.addEventListener('click', function(e){
    var tab = e.target.closest('.yyk-vdr-tab');
    if(tab) {
      setVdrTab(tab.getAttribute('data-vdr-tab'));
      return;
    }
    if(e.target && e.target.id === 'vdrRunQualification'){
      runQualification();
      return;
    }
    if(e.target && e.target.id === 'vdrDownloadQualification'){
      downloadReport();
      return;
    }
  });

  window.setVdrTab = setVdrTab;
  window.runQualification = runQualification;
  window.downloadReport = downloadReport;

  // Ghost Text Cleaner
  var ghostPatterns=[/^\s*integrity\s+controls\s*$/i,/^\s*delivery\s*$/i,/^\s*;?var\s+blob\s*=\s*new\s+Blob/i,/YONGYEOKYO_Preliminary_Supplier_Qualification_Report\.html/i];
  function isInsideAllowedCode(node){
    var el=node.parentElement;
    while(el){
      var tag=(el.tagName||'').toLowerCase();
      if(tag==='script'||tag==='style'||tag==='pre'||tag==='code'||tag==='textarea') return true;
      el=el.parentElement;
    }
    return false;
  }
  function cleanGhostText(root){
    if(!root||!document.body) return;
    var walker=document.createTreeWalker(root,NodeFilter.SHOW_TEXT,{acceptNode:function(node){
      if(isInsideAllowedCode(node)) return NodeFilter.FILTER_REJECT;
      var t=node.nodeValue||'';
      if(!t.trim()) return NodeFilter.FILTER_REJECT;
      return ghostPatterns.some(function(rx){return rx.test(t.trim());})?NodeFilter.FILTER_ACCEPT:NodeFilter.FILTER_REJECT;
    }});
    var targets=[];
    while(walker.nextNode()) targets.push(walker.currentNode);
    targets.forEach(function(node){
      var p=node.parentElement;
      if(p){
        p.classList.add('yyk-ghost-text');
        p.setAttribute('aria-hidden','true');
      }else{
        node.nodeValue='';
      }
    });
  }
  function normalizeActionTitles(){
    document.querySelectorAll('button,a,[role="button"],.micro-btn,.nav-link,.sogo-primary-cta').forEach(function(el){
      ['data-label','data-tooltip','data-title','data-hover','data-hint'].forEach(function(attr){
        if(el.hasAttribute(attr)) el.removeAttribute(attr);
      });
    });
  }
  function run(){cleanGhostText(document.body);normalizeActionTitles();}
  if(document.readyState==='loading'){document.addEventListener('DOMContentLoaded',run,{once:true});}else{run();}
  var mo=new MutationObserver(function(muts){
    var need=false;
    muts.forEach(function(m){if(m.addedNodes&&m.addedNodes.length) need=true;});
    if(need) window.requestAnimationFrame(run);
  });
  if(document.body) mo.observe(document.body,{childList:true,subtree:true});
})();
